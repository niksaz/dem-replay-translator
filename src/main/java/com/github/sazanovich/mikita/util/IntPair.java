package com.github.sazanovich.mikita.util;

/**
 * Class that represents pair of ints.
 */
public class IntPair {
    /** First element of pair. */
    public int fst;
    /** Second element of pair. */
    public int snd;

    /**
     * Constructs new pair from given ints.
     * @param a1 first element
     * @param a2 second element
     */
    public IntPair(int a1, int a2) {
        fst = a1;
        snd = a2;
    }
}
